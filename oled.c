/*************将char转成字符串函数**************/
int8 *uchartostr(uint16 num)
{

	uint8 xoo,xx,xo,x,n;
	xoo = num/100;
	xx = num%100; 
	xo = xx/10;
	x = xx%10;
	n = 0;
	if(xoo != 0)
	{
		str[n] = xoo+'0'; //将字符0即变为字符
                n++;		
	}
	if(!(xoo==0&xo==0))
	{str[n]=xo+'0';n++;}
	str[n]=x+'0';
	n++;
	str[n] = '\0';
	return str;
}


